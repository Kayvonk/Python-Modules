# 04_input_and_greeting.py
# 👋 Let's ask the user questions and say hello!

# 🧠 Use input() to ask for the user's name
name = input("What is your name? ")

# 🧠 Ask for the user's favorite color
color = input("What is your favorite color? ")

# 🧠 Ask for the user's favorite animal
animal = input("What's your favorite animal? ")

# 🖨️ Now print a fun greeting using their answers
print(f"Hi {name}! It's cool that your favorite color is {color}!")
print(f"{animal}s are awesome! 🐾")

# ✨ Bonus:
# Ask the user for their mood, and print a fun message based on it.

mood = input("How are you feeling today? ")

print(f"I'm glad to hear you're feeling {mood}! 😊")

