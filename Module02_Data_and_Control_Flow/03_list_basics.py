# A list holds many values in order, like a shopping list or favorites.

# Create a list of fruits
fruits = ["apple", "banana", "cherry"]

# Print the whole list
print(fruits)

# Print the first fruit (index 0)
print(fruits[0])

# Print the last fruit using negative index
print(fruits[-1])

# Add a new fruit to the list
fruits.append("orange")
print(fruits)

# Change the second fruit
fruits[1] = "blueberry"
print(fruits)

